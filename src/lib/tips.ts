/** Stable within a calendar day, so it doesn't change on every re-render. */
export function getTipOfTheDay(logDate: string): string {
  let hash = 0
  for (let i = 0; i < logDate.length; i++) {
    hash = (hash * 31 + logDate.charCodeAt(i)) >>> 0
  }
  return TIPS[hash % TIPS.length]
}

